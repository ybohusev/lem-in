#include "libft.h"

double		ft_dsqrt(int nb)
{
	double	x;
	double	y;

	if ((x = ft_sqrt(nb)) != 0)
		return (x);
	x = 0.5 * (1 + (nb / 1));
	y = x;
	x = 0.5 * (x + (nb / x));
	while ((y - x) > 0)
	{
		y = x;
		x = 0.5 * (x + (nb / x));
	}
	return (x);
}
